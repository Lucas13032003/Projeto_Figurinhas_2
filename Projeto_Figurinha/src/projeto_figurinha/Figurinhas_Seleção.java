package projeto_figurinha;

public class Figurinhas_Seleção {
	private String seleção;
	private String jogador;
	private String posição;
	
	//gets e sets
	
	public String getSeleção () {
		return seleção;
	}
	public void setSeleção (){
		this.seleção = seleção;
	}
	public String getJogador () {
		return jogador;
	}
	public void setJogador (){
		this.jogador = jogador;
	}
	public String getPosição () {
		return posição;
	}
	public void setPosição (){
		this.posição = posição;
	}
}
